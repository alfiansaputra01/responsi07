    protected final void datatable(){
        Object[] baris = {"Judul","Alur","Penokohan","Akting","Nilai"};
        model =  new DefaultTableModel(null, baris);
        tb_movie.setModel(model);
        
        try {
            ResultSet hasil = control.tampilMovie();
            while(hasil.next()){
                 String judul = hasil.getString("judul");
                 String alur = hasil.getString("alur");
                 String penokohan = hasil.getString("penokohan");
                 String akting = hasil.getString("akting");
                 String nilai = hasil.getString("nilai");
                 
                 String[] data={judul,alur,penokohan,akting,nilai};
                 
                 model.addRow(data);
                 
             }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"Gagal Menampilkan"+e);
        }  
    }
