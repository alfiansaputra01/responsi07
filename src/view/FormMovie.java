    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        control.inputMovie(txtJudul.getText(), txtAlur.getText(), txtTokoh.getText(), txtAkting.getText());
        boolean hasil = control.insertMovie();
       if(hasil){
           JOptionPane.showMessageDialog(null,"Data berhasil Disimpan");
            datatable();
       }else{
            JOptionPane.showMessageDialog(null,"Gagal Menyimpan Data");
        }
       txtKosong();
        
        DefaultTableModel tableModel = (DefaultTableModel) tb_movie.getModel();
        String [] dataNilai = new String[6];
        double jml = Float.valueOf(txtAlur.getText())
                                +Float.valueOf(txtTokoh.getText())
                                +Float.valueOf(txtAkting.getText());
        dataNilai[0]=String.valueOf(1 + tb_movie.getRowCount());
        dataNilai[1]=txtJudul.getText();
        dataNilai[2]=txtAlur.getText();
        dataNilai[3]=txtTokoh.getText();
        dataNilai[4]=txtAkting.getText();
        dataNilai[5]=String.valueOf(jml/3);
        tableModel.addRow(dataNilai);
    }//GEN-LAST:event_btnAddActionPerformed

